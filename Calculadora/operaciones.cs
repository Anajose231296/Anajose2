using System;
using Gtk;
public class operaciones
{
    public double suma(double valor_a, double valor_b)
    {
        return valor_a + valor_b;
    }

    public double resta(double valor_a, double valor_b)
    {
        return valor_a - valor_b;
    }

    public double multiplicar(double valor_a, double valor_b)
    {
        return valor_a * valor_b;
    }

    public double dividir(double valor_a, double valor_b)
    {
        return valor_a / valor_b;
    }
}